export function getMaxScore(questionId: number): number {
  return questionId >= 64 ? 5 : 7;
}

/**
 * „Ç´„ÉÜ„Ç¥„É™„Çπ„Ç≥„Ç¢„ÇíË®àÁÆó
 */
export function calculateCategoryScore(answers: SCMAnswer[], categoryId: SCMCategoryId): SCMCategoryScore {
  const category = SCM_CATEGORIES[categoryId];
  const categoryAnswers = answers.filter(a => 
    category.questions.includes(a.questionId)
  );
  
  const totalScore = categoryAnswers.reduce((sum, a) => sum + a.score, 0);
  const maxScore = category.questions.reduce((sum, qId) => sum + getMaxScore(qId), 0);
  
  return {
    categoryId,
    totalScore,
    maxScore,
    percentage: Math.round((totalScore / maxScore) * 100),
    questionCount: category.questions.length,
  };
}

/**
 * ÂÖ®‰Ωì„ÅÆ„Çπ„Ç≥„Ç¢„ÇíË®àÁÆó
 */
export function calculateTotalResult(answers: SCMAnswer[], userId: string): Omit<SCMResult, 'id' | 'takenAt'> {
  const categoryScores = SCM_CATEGORY_IDS.map(catId => 
    calculateCategoryScore(answers, catId)
  );
  
  const totalScore = categoryScores.reduce((sum, c) => sum + c.totalScore, 0);
  const maxScore = categoryScores.reduce((sum, c) => sum + c.maxScore, 0);
  
  // Âº∑„Åø„ÉªÂº±„Åø„ÇíÊäΩÂá∫Ôºà‰∏ä‰Ωç2„ÄÅ‰∏ã‰Ωç2Ôºâ
  const sortedCategories = [...categoryScores].sort((a, b) => b.percentage - a.percentage);
  const strengths = sortedCategories.slice(0, 2).map(c => c.categoryId);
  const weaknesses = sortedCategories.slice(-2).reverse().map(c => c.categoryId);
  
  return {
    userId,
    totalScore,
    totalPercentage: Math.round((totalScore / maxScore) * 100),
    categoryScores,
    answers,
    strengths,
    weaknesses,
  };
}

